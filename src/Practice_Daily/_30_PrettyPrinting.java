package Practice_Daily;

public class _30_PrettyPrinting {
    public static void main(String[] args) {
        float a = 432.1245f;
        System.out.printf("%.2f",a);
        System.out.println();
        System.out.printf("%.3f",Math.PI);
        System.out.println();
        System.out.printf("hello my name is %s sena reddy", "indra");
    }
}
